package ch04.marioKart.dto;

public class KartDTO {
	// 필드
	public String name;
	public double weight;
	public double speed;
	public double acceleration;
	public double handling;
	public double friction;
	
	
	
	public KartDTO(String name, double weight, double speed, double acceleration, double handling, double friction) {
		this.name = name;
		this.weight = weight;
		this.speed = speed;
		this.acceleration = acceleration;
		this.handling = handling;
		this.friction = friction;
	}
	// 생성자
	
	
	// 메서드

}
